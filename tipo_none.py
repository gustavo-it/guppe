"""
Tipo None

O tipo de dado None em python representa o tipo sem tipo, ou poderia ser
conhecido também como tipo vazio, porém falar que é um tipo sem tipo é mais apropriado.

OBS: O tipo none é sempre especificado com a primeira letra maiúscula.

Certo: None
Errado: none

Quando utilizamos ?

- Podemos utilizar o None, quando queremos criar uma variável e inicializa-la com um
tipo sem tipo, antes de recebermos um valor final.

OBS: O tipo None em python é sempre considerado como False.
"""
numeros = None
print(type(numeros))

numeros = 1, 2
print(type(numeros))
